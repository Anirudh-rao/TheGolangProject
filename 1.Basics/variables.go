package main

import "fmt"

func main() {

	//Variable Declaration
	var a = "initial"
	fmt.Println(a)

	//integer declaration
	var b, c int = 1, 2
	fmt.Println(b, c)

	//Boolean Declartion
	var d = true
	fmt.Println(d)

	//integer declaration
	var e int
	fmt.Println(e)

	//String initialization
	f := "apple"
	fmt.Println(f)

}
